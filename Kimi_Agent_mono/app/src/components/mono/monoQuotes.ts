/**
 * 根据场景获取随机语录
 */
export function getMonoQuote(scene: keyof typeof monoQuotes): string {
  const quotes = monoQuotes[scene];
  return quotes[Math.floor(Math.random() * quotes.length)];
}

/**
 * 根据时间段获取问候语
 */
export function getGreeting(): string {
  const hour = new Date().getHours();

  if (hour < 6) {
    return '夜深了，早点休息吧！明天还有很多事要做呢。';
  } else if (hour < 9) {
    return '早安！新的一天开始了，让我们一起加油！';
  } else if (hour < 12) {
    return '上午好！趁着精力充沛，先做最重要的事吧。';
  } else if (hour < 14) {
    return '中午好！休息一下，下午继续！';
  } else if (hour < 18) {
    return '下午好！保持专注，快到收获的时候了。';
  } else if (hour < 22) {
    return '晚上好！今天辛苦了，让我们复盘一下吧。';
  } else {
    return '夜已深，明天再继续吧，休息也很重要！';
  }
}
